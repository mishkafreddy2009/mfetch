#!/usr/bin/env python
import platform
import getpass
import socket


class MinimalFetch():
    def get_ascii(self):
        self.ascii_logo = f"""
     _=_     {self.username}@{self.hostname}
   q(-_-)p   distro: {self.distro_name}
   '_) (_`   uptime: {int(self.uptime_minutes)} min
   /__/  \   
 _(<_   / )_ 
(__\_\_|_/__) 
        """

        print(self.ascii_logo)

    def get_uptime(self):
        with open('/proc/uptime', 'r') as f:
            self.uptime_minutes = float(f.readline().split()[0]) // 60

    def get_username(self):
        self.username = getpass.getuser()
        self.hostname = socket.gethostname()

    def get_distro_name(self):
        self.distro_name = list(platform.freedesktop_os_release().values())[0].lower()


def get_fetch():
    mfetch = MinimalFetch()
    mfetch.get_distro_name()
    mfetch.get_uptime()
    mfetch.get_username()
    mfetch.get_ascii()

def main():
    get_fetch()

if __name__ == "__main__":
    main()


